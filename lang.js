/* ── Engine ─────────────────────────────────────────── */
let _lang = localStorage.getItem('charming_lang') || 'he';

function t(key, fallback) {
  if (_lang === 'en' && _en[key]) return _en[key];
  return fallback || '';
}

function getLang() { return _lang; }

function applyLang() {
  const lang = _lang;
  document.documentElement.lang = lang;
  document.documentElement.dir  = lang === 'he' ? 'rtl' : 'ltr';

  /* text content */
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (!el.dataset.he) el.dataset.he = el.textContent;
    el.textContent = lang === 'en' ? (_en[key] || el.dataset.he) : el.dataset.he;
  });

  /* innerHTML (for elements with nested HTML) */
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (!el.dataset.heHtml) el.dataset.heHtml = el.innerHTML;
    el.innerHTML = lang === 'en' ? (_en[key] || el.dataset.heHtml) : el.dataset.heHtml;
  });

  /* placeholders */
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.dataset.i18nPh;
    if (!el.dataset.hePh) el.dataset.hePh = el.placeholder;
    el.placeholder = lang === 'en' ? (_en[key] || el.dataset.hePh) : el.dataset.hePh;
  });

  /* update dropdown active state */
  document.querySelectorAll('.lang-dropdown-menu button[data-lang]').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

function setLang(lang) {
  _lang = lang;
  localStorage.setItem('charming_lang', lang);
  applyLang();
  // Refresh navbar greeting (שלום / Hello) without page reload
  if (typeof window._updateAuthNavbar === 'function') window._updateAuthNavbar();
  // Re-render product catalogs to show bilingual names/descriptions
  if (typeof window._rerenderProducts === 'function') window._rerenderProducts();
  // Rebuild accessibility widget for new language
  if (typeof window._rebuildA11yWidget === 'function') window._rebuildA11yWidget();
}

function toggleLang() {
  setLang(_lang === 'he' ? 'en' : 'he');
}

/* auto-init */
document.addEventListener('DOMContentLoaded', () => {
  applyLang();
  document.documentElement.classList.remove('lang-loading');

  /* Language dropdown — open/close */
  document.querySelectorAll('.lang-dropdown-btn').forEach(btn => {
    btn.addEventListener('click', e => {
      e.stopPropagation();
      const dd = btn.closest('.lang-dropdown');
      // Close all other dropdowns
      document.querySelectorAll('.lang-dropdown.open').forEach(d => { if (d !== dd) d.classList.remove('open'); });
      dd.classList.toggle('open');
    });
  });

  /* Language dropdown — select language */
  document.querySelectorAll('.lang-dropdown-menu button[data-lang]').forEach(btn => {
    btn.addEventListener('click', e => {
      e.stopPropagation();
      setLang(btn.dataset.lang);
      document.querySelectorAll('.lang-dropdown.open').forEach(d => d.classList.remove('open'));
    });
  });

  /* Close dropdown on outside click */
  document.addEventListener('click', () => {
    document.querySelectorAll('.lang-dropdown.open').forEach(d => d.classList.remove('open'));
  });
});

/* expose globally */
window.t        = t;
window.getLang   = getLang;
window.applyLang = applyLang;
window.setLang   = setLang;
window.toggleLang = toggleLang;
